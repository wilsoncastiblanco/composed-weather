val pathParser = PathParser()

fun String.toPath(scale: PathScale): Path {
    val weatherPathParser = pathParser.parsePathString(this)
    val path = weatherPathParser.toPath()
    return path.asAndroidPath().also {
        it.transform(Matrix().apply {
            this.setScale(scale.x, scale.y, scale.px, scale.py)
        })
    }.asComposePath()
}

